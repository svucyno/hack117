function performI18nTranslation() {
    const lang = localStorage.getItem('agripredict_lang');
    if (!lang || lang === 'en') return;

    // Recursive DOM replacement to protect HTML tags
    function translateNode(node) {
        if (node.nodeType === Node.TEXT_NODE) {
            let originalText = node.nodeValue;
            let replacedText = originalText;
            
            // Do a simple substring replacement for each key in dictionary
            Object.keys(i18nDict).forEach(engKey => {
                if (replacedText.includes(engKey) && i18nDict[engKey][lang]) {
                    // Quick fix to avoid translating substrings in other words if it exactly matches or forms the bulk of the content
                    replacedText = replacedText.replace(engKey, i18nDict[engKey][lang]);
                }
            });

            if (replacedText !== originalText) {
                node.nodeValue = replacedText;
            }
        } else if (node.nodeType === Node.ELEMENT_NODE) {
            // Ignore script and style tags completely
            if (node.tagName === 'SCRIPT' || node.tagName === 'STYLE') return;

            // Also translate placeholders
            if (node.tagName === 'INPUT' || node.tagName === 'TEXTAREA') {
                if (node.placeholder) {
                    let replacedPlaceholder = node.placeholder;
                    Object.keys(i18nDict).forEach(engKey => {
                        if (replacedPlaceholder.includes(engKey) && i18nDict[engKey][lang]) {
                            replacedPlaceholder = replacedPlaceholder.replace(engKey, i18nDict[engKey][lang]);
                        }
                    });
                    if (replacedPlaceholder !== node.placeholder) {
                        node.placeholder = replacedPlaceholder;
                    }
                }
            }

            // Check children
            for (let child of node.childNodes) {
                translateNode(child);
            }
        }
    }

    translateNode(document.body);
}

// Ensure execution immediately and after DOM content loads just to be safe
document.addEventListener('DOMContentLoaded', () => setTimeout(performI18nTranslation, 100)); // small timeout ensures UI finishes injecting

// Allow manual trigger if page elements load dynamically
window.triggerTranslation = performI18nTranslation;
